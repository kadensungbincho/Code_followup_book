public class Item15 {
    /*
        Minimize the accessibility of classes and members

        information hiding or encapsulation

        - it decouples the components that comprise a system, allowing them to be developed
            , tested, optimized, used, understood, and modified in isolation.

        - While information hiding does not, in and of itself, cause good performance, it enables
            effective performance tuning: once a system is complete and profiling has determined which
            components are causing performance problems, those components can be optimized without affecting
            the correctness of others.

        - make each class or member as inaccessible as possible

        - it is wrong for a class to have a public static final array field, or an accessor that returns such
            a field


     */
}
